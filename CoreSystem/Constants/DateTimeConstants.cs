namespace CoreSystem.Constants
{
    /// <summary>
    /// DateTime constants.
    /// </summary>
    public static class DateTimeConstants
    {
        public static string Date { get { return "dd/MM/yyyy"; } }
        public static string Time { get { return "hh:mm:ss"; } }
        public static string TimeAMPM { get { return "hh:mmtt"; } }
        public static string DateTime { get { return "dd/MM/yyyy hh:mm:ss"; } }
        public static string WeekDate { get { return "dddd, dd MMMM yyyy"; } }
        public static string WeekShortDate { get { return "dddd dd/MM"; } }
    }
}